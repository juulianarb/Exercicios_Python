# Exercicio que calcula a área do trapézio

baseMaior = float(input("Digite o valor da base maior, para calcular a área do trapézio:"))
baseMenor = float(input("Digite o valor da base menor, para calcular a área do trapézio:"))
altura = float(input("Digite o valor da altura, para calcular a área do trapézio:"))

area = (baseMaior + baseMenor) * altura / 2

print("A área do trapézio é de:" , area , "mt²")



