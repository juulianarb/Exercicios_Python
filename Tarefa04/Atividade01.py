# Faça um programa que receba a idade do usuário e imprima se ele é maior de idade ou não.

idade = int(input("Digite sua idade, para saber se é maior de idade:"))

if idade >= 18:
    print("Você é maior de idade!")
else:
    print("Você não é maior de idade")